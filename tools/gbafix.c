// gbafix.c — Minimal GBA ROM header fixer
// Writes the Nintendo logo, game title, and corrects the header checksum
// so the ROM boots on real hardware (and picky emulators).
//
// Usage: gbafix input.gba output.gba
//
// This is a stripped-down reimplementation. The original gbafix is part
// of devkitPro but we bundle this so the project builds with just
// `brew install arm-none-eabi-gcc arm-none-eabi-binutils` — no tap needed.

#include <stdio.h>
#include <stdint.h>
#include <stdlib.h>
#include <string.h>

// The Nintendo logo bitmap that must appear at offset 0x04-0x9F
// (Required for the BIOS boot sequence to accept the ROM)
static const uint8_t nintendo_logo[156] = {
    0x24,0xFF,0xAE,0x51,0x69,0x9A,0xA2,0x21,0x3D,0x84,0x82,0x0A,
    0x84,0xE4,0x09,0xAD,0x11,0x24,0x8B,0x98,0xC0,0x81,0x7F,0x21,
    0xA3,0x52,0xBE,0x19,0x93,0x09,0xCE,0x20,0x10,0x46,0x4A,0x4A,
    0xF8,0x27,0x31,0xEC,0x58,0xC7,0xE8,0x33,0x82,0xE3,0xCE,0xBF,
    0x85,0xF4,0xDF,0x94,0xCE,0x4B,0x09,0xC1,0x94,0x56,0x8A,0xC0,
    0x13,0x72,0xA7,0xFC,0x9F,0x84,0x4D,0x73,0xA3,0xCA,0x9A,0x61,
    0x58,0x97,0xA3,0x27,0xFC,0x03,0x98,0x76,0x23,0x1D,0xC7,0x61,
    0x03,0x04,0xAE,0x56,0xBF,0x38,0x84,0x00,0x40,0xA7,0x0E,0xFD,
    0xFF,0x52,0xFE,0x03,0x6F,0x95,0x30,0xF1,0x97,0xFB,0xC0,0x85,
    0x60,0xD6,0x80,0x25,0xA9,0x63,0xBE,0x03,0x01,0x4E,0x38,0xE2,
    0xF9,0xA2,0x34,0xFF,0xBB,0x3E,0x03,0x44,0x78,0x00,0x90,0xCB,
    0x88,0x11,0x3A,0x94,0x65,0xC0,0x7C,0x63,0x87,0xF0,0x3C,0xAF,
    0xD6,0x25,0xE4,0x8B,0x38,0x0A,0xAC,0x72,0x21,0xD4,0xF8,0x07,
};

// Game title to write (12 bytes, padded with spaces)
static const char game_title[12] = "AS&S GAME   ";
// Game code (4 bytes)
static const char game_code[4]   = "ASSG";
// Maker code (2 bytes)
static const char maker_code[2]  = "HB";

int main(int argc, char *argv[]) {
    if (argc < 3) {
        fprintf(stderr, "Usage: %s <input.gba> <output.gba>\n", argv[0]);
        return 1;
    }

    FILE *fin = fopen(argv[1], "rb");
    if (!fin) {
        perror("Cannot open input ROM");
        return 1;
    }

    // Read entire input file.
    fseek(fin, 0, SEEK_END);
    long size = ftell(fin);
    if (size < 192) {
        fprintf(stderr, "ROM too small (need at least 192 bytes for header)\n");
        fclose(fin);
        return 1;
    }
    uint8_t *rom = malloc(size);
    fseek(fin, 0, SEEK_SET);
    fread(rom, 1, size, fin);
    fclose(fin);

    uint8_t *header = rom;

    // Write Nintendo logo at offset 0x04
    memcpy(header + 0x04, nintendo_logo, 156);

    // Write game title at offset 0xA0 (12 bytes)
    memcpy(header + 0xA0, game_title, 12);

    // Write game code at offset 0xAC (4 bytes)
    memcpy(header + 0xAC, game_code, 4);

    // Write maker code at offset 0xB0 (2 bytes)
    memcpy(header + 0xB0, maker_code, 2);

    // Fixed value at offset 0xB2
    header[0xB2] = 0x96;

    // Main unit code at offset 0xB3
    header[0xB3] = 0x00;

    // Device type at offset 0xB4
    header[0xB4] = 0x00;

    // Reserved area at offset 0xB5-0xBB (7 bytes, zero)
    memset(header + 0xB5, 0, 7);

    // Software version at offset 0xBC
    header[0xBC] = 0x01;

    // Compute header checksum (offset 0xBD)
    // Checksum = -sum(header[0xA0..0xBC]) - 0x19
    uint8_t checksum = 0;
    for (int i = 0xA0; i <= 0xBC; i++) {
        checksum -= header[i];
    }
    checksum -= 0x19;
    header[0xBD] = checksum;

    // Reserved at offset 0xBE-0xBF
    header[0xBE] = 0x00;
    header[0xBF] = 0x00;

    // Write the fixed ROM to output.
    FILE *fout = fopen(argv[2], "wb");
    if (!fout) {
        perror("Cannot open output ROM");
        free(rom);
        return 1;
    }
    fwrite(rom, 1, size, fout);
    fclose(fout);
    free(rom);

    printf("gbafix: %s — header fixed (checksum=0x%02X)\n", argv[2], checksum);
    return 0;
}
