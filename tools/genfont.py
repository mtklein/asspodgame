    tiles = []
    for code in range(32, 127):
        ch = chr(code)
        if ch in FONT:
            tiles.extend(bitmap_to_4bpp(FONT[ch]))
        else:
            tiles.extend([0] * 8)

    print("// font_data.h — Auto-generated by genfont.py")
    print("// DO NOT EDIT BY HAND")
