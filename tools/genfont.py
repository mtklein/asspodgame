def bitmap_to_4bpp(bitmap):
    """Convert 8 bytes of 1bpp data to 8 u32 words of 4bpp"""
    words = []
    for row in bitmap:
        word = 0
        for bit in range(8):
            if row & (0x80 >> bit):
                word |= (0xF << (bit * 4))  # Color 15 (white)
        words.append(word)
    return words

