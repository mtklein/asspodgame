const u32* portrait_find(const char *speaker) {
    if (!speaker) return 0;
    for (int i = 0; portrait_table[i].speaker; i++) {
        const char *a = speaker, *b = portrait_table[i].speaker;
        int match = 1;
        while (*a && *b) { if (*a++ != *b++) { match = 0; break; } }
        if (match && *a == *b) return portrait_table[i].data;
    }
    return 0;
}
